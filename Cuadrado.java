public class Cuadrado {
   
   
    public double CalcularArea(double num1){
    double resultado = 0;
    resultado = num1 * num1;
    return resultado;
    
   
    
    }
    public double CalcularPerimetro(double num1){
    double resultado = 0;
    resultado = num1 * 4;
    return resultado;    
    }
}
